package licao1;

public class Soma {

    public static void main(String[] args) {
     int a = 15;
      int b = 10;
      int soma = a + b;
      int subtracao = a - b;
      int multiplicacao = a * b;
      int divisao = a / b;
      System.out.println("a + b: "+soma);
      System.out.println("a - b: "+subtracao);
      System.out.println("a * b: "+multiplicacao);
      System.out.println("a / b: "+divisao);
    }
    
}
